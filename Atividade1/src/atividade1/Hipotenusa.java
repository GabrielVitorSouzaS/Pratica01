public class Hipotenusa {
    
    //Atributos
     double hipotenusa;
     double c1;
     double c2;
     
     
     //Metodos 
     
     public double CalculaHipotenusa( double c1, double c2){
         
         hipotenusa = Math.sqrt((c1*c1)+(c2*c2));
         
         return hipotenusa;
         
     }
     
     
    
    
    
    
    
}
